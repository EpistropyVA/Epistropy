def is_prime_power(n):
    """Return (True, p, k) if n = p^k for prime p, k>=1; else (False, None, None)."""
    if n < 2:
        return False, None, None
    f = factorint(n)
    if len(f) == 1:
        p = list(f.keys())[0]
        k = f[p]
        return True, p, k
    return False, None, None


