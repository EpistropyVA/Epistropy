def compute_GL3F2_order():
    """|GL(3, F_2)| = (2^3-1)(2^3-2)(2^3-4) = 7*6*4 = 168"""
    q = 2
    n = 3
    order = 1
    for i in range(n):
        order *= (q ** n - q ** i)
    return order


