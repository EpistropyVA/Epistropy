def construct_pg2(q):
    """
    Construct PG(2, F_q) combinatorially using homogeneous coordinates
    over GF(q) for prime q (direct mod-p arithmetic). For prime powers
    that are not prime (4,8,9), we instead verify via the COMBINATORIAL
    incidence structure derived from the known line-counting formulas,
    cross-checked against an explicit construction over GF(p) for the
    prime cases, and an abstract (non-coordinate) verification for the
    prime-power cases using the general theorem that PG(2,F_q) exists
    for every prime power q with these exact parameters.
    """
    is_pp, p, k = is_prime_power(q)
    assert is_pp, f"{q} is not a prime power, no field F_{q} exists"

    if k == 1:
        # prime field F_p: build via homogeneous coords mod p
        return _construct_pg2_prime(q)
    else:
        # prime power but not prime (4, 8, 9, ...): use GF(p^k) via
        # sympy's galoistools for explicit field elements, then build
        # homogeneous coordinate points/lines over that field.
        return _construct_pg2_primepower(p, k)


