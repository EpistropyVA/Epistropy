def cascade_length(q0):
    """Starting at q0 (must be prime power), repeatedly apply q -> q+1
    while q+1 remains a prime power. Return the chain (list) and its length
    (number of fields reached, i.e. chain length including q0)."""
    chain = [q0]
    q = q0
    while True:
        nxt = q + 1
        is_pp, _, _ = is_prime_power(nxt)
        if is_pp:
            chain.append(nxt)
            q = nxt
        else:
            break
    return chain


def verify_part4():
    sep("Part 4: Cascade chain analysis — maximal chain length over all starting q0")
    chain_from_2 = cascade_length(2)
    expected_chain = [2, 3, 4, 5]
    match = (chain_from_2 == expected_chain)
    print(f"Chain from q0=2: {chain_from_2} (length {len(chain_from_2)}) "
          f"-> {'PASS' if match else 'FAIL'}")
    assert match, f"Expected cascade {expected_chain}, got {chain_from_2}"

    # confirm break at 6
    is_pp_6, _, _ = is_prime_power(6)
    print(f"q+1=6 is prime power: {is_pp_6} -> {'PASS' if not is_pp_6 else 'FAIL'} (6=2*3 breaks cascade)")
    assert not is_pp_6

    # scan all prime powers up to 100 as starting points, find max chain length
    results = {}
    for q0 in PRIME_POWERS_UPTO_100:
        results[q0] = cascade_length(q0)

    max_len = max(len(c) for c in results.values())
    best_starts = [q0 for q0, c in results.items() if len(c) == max_len]

    print(f"\nScanned {len(PRIME_POWERS_UPTO_100)} prime-power starting points up to 100.")
    print(f"Max chain length found: {max_len}, achieved by starting q0 in {best_starts}")

    # print a few notable chains
    for q0 in [2, 3, 4, 7, 8, 9, 13, 16, 27]:
        if q0 in results:
            print(f"  q0={q0}: chain={results[q0]} (len={len(results[q0])})")

    only_q2_is_max = (best_starts == [2])
    print(f"\nq0=2 uniquely achieves the maximum chain length: "
          f"{'PASS' if only_q2_is_max else 'FAIL (tie or another q0 wins)'}")
    assert len(results[2]) == max_len, "q0=2 must achieve the maximal chain length"

    print(f"\nPart 4 overall: PASS (q0=2 chain length={len(chain_from_2)}, "
          f"global max={max_len}, unique starter(s) achieving max={best_starts})")
    return True, only_q2_is_max, best_starts


