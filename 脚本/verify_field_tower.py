    for q in qs:
        is_pp, p, k = is_prime_power(q)
        assert is_pp, f"q={q} must be a prime power"
        points, lines = construct_pg2(q)
        n_points = len(points)
        n_lines = len(lines)
        expected = q * q + q + 1

        # points per line
        pts_per_line = set(len(L) for L in lines)
        # lines through each point
        lines_per_point = {}
        for i in range(n_points):
            lines_per_point[i] = sum(1 for L in lines if i in L)
        lpp_set = set(lines_per_point.values())

        ok_points = (n_points == expected)
        ok_lines = (n_lines == expected)
        ok_ppl = (pts_per_line == {q + 1})
        ok_lpp = (lpp_set == {q + 1})

        # two distinct points determine exactly one common line
        ok_two_pts_one_line = True
        for i, j in itertools.combinations(range(min(n_points, n_points)), 2):
            common = [L for L in lines if i in L and j in L]
            if len(common) != 1:
                ok_two_pts_one_line = False
                break

        # two distinct lines meet in exactly one point
        ok_two_lines_one_point = True
        for L1, L2 in itertools.combinations(lines, 2):
            inter = L1 & L2
            if len(inter) != 1:
                ok_two_lines_one_point = False
                break

        status = all([ok_points, ok_lines, ok_ppl, ok_lpp, ok_two_pts_one_line, ok_two_lines_one_point])
        all_pass &= status
        print(f"q={q} (p={p}^{k}): points={n_points} lines={n_lines} expected={expected} "
              f"pts/line={pts_per_line} lines/pt={lpp_set} "
              f"two-pts->1-line={ok_two_pts_one_line} two-lines->1-pt={ok_two_lines_one_point} "
              f"-> {'PASS' if status else 'FAIL'}")

        assert ok_points, f"q={q}: point count mismatch"
        assert ok_lines, f"q={q}: line count mismatch"
        assert ok_ppl, f"q={q}: points-per-line mismatch"
        assert ok_lpp, f"q={q}: lines-per-point mismatch"
        assert ok_two_pts_one_line, f"q={q}: two points do not determine unique line"
        assert ok_two_lines_one_point, f"q={q}: two lines do not meet in unique point"

    print(f"\nPart 1 overall: {'PASS' if all_pass else 'FAIL'}")
    return all_pass


