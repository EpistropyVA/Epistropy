def mod_inverse_field(a, p):
    return pow(a, p - 2, p)


def _construct_pg2_prime(p):
    """Homogeneous coordinates over Z/p. Points = nonzero vectors mod scalar mult."""
    elems = list(range(p))
    nonzero_vecs = [v for v in itertools.product(elems, repeat=3) if v != (0, 0, 0)]

    def normalize(v):
        for c in v:
            if c != 0:
                inv = pow(c, p - 2, p)
                break
        return tuple((x * inv) % p for x in v)

    points = sorted(set(normalize(v) for v in nonzero_vecs))
    point_index = {pt: i for i, pt in enumerate(points)}

    # lines are also normalized triples (a,b,c) representing ax+by+cz=0
    lines = points  # same parametrization (duality)

    def incident(point, line):
        a, b, c = line
        x, y, z = point
        return (a * x + b * y + c * z) % p == 0

    line_point_sets = []
    for line in lines:
        pts_on_line = frozenset(point_index[pt] for pt in points if incident(pt, line))
        line_point_sets.append(pts_on_line)

    return points, line_point_sets


def _gf_pk_elements(p, k):
    """Build GF(p^k) elements as tuples (coeffs of polynomial basis), using
    sympy to find an irreducible polynomial of degree k over GF(p), and
    perform arithmetic via polynomial mod reduction. Returns:
      elements: list of tuples (length k) representing field elements
      add(a,b), mul(a,b): functions
    """
    from sympy import GF, Poly, symbols
    from sympy.polys.galoistools import gf_irreducible, gf_add, gf_mul, gf_rem

    x = symbols("x")
    # get an irreducible monic poly of degree k over GF(p) as list of coeffs (sympy gf format, highest degree first)
    modulus = gf_irreducible(k, p, GF(p))

    elements = list(itertools.product(range(p), repeat=k))

    def to_gf(t):
        # tuple (highest..lowest) matches sympy gf list convention
        return list(t)

    def from_gf(lst):
        lst = [c % p for c in lst]
        while len(lst) < k:
            lst = [0] + lst
        return tuple(lst[-k:])

    def add(a, b):
        r = gf_add(to_gf(a), to_gf(b), p, GF(p))
        return from_gf(r)

    def mul(a, b):
        prod = gf_mul(to_gf(a), to_gf(b), p, GF(p))
        rem = gf_rem(prod, modulus, p, GF(p))
        return from_gf(rem)

    return elements, add, mul, modulus


def _construct_pg2_primepower(p, k):
    """Construct PG(2, F_{p^k}) for k>1 using explicit GF(p^k) arithmetic."""
    elements, add, mul, modulus = _gf_pk_elements(p, k)
    zero = tuple([0] * k)

    # precompute multiplicative inverses via brute force (field is small enough: q<=9)
    nonzero_elems = [e for e in elements if e != zero]
    inv_table = {}
    for a in nonzero_elems:
        for b in nonzero_elems:
            if mul(a, b) == tuple([0] * (k - 1) + [1]):
                inv_table[a] = b
                break
    one = tuple([0] * (k - 1) + [1])
    assert all(e in inv_table for e in nonzero_elems), "inverse table incomplete"

    vecs = [v for v in itertools.product(elements, repeat=3) if v != (zero, zero, zero)]

    def normalize(v):
        for c in v:
            if c != zero:
                inv = inv_table[c]
                break
        return tuple(tuple(mul(x, inv)) for x in v)

    points = sorted(set(normalize(v) for v in vecs))
    point_index = {pt: i for i, pt in enumerate(points)}

    def dot_zero(line, point):
        a, b, c = line
        x, y, z = point
        s = zero
        for u, v in zip((a, b, c), (x, y, z)):
            s = add(s, mul(u, v))
        return s == zero

    lines = points
    line_point_sets = []
    for line in lines:
        pts_on_line = frozenset(point_index[pt] for pt in points if dot_zero(line, pt))
        line_point_sets.append(pts_on_line)

    return points, line_point_sets


