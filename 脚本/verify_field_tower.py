    points, lines = construct_pg2(2)
    n = len(points)
    assert n == 7

    M = np.zeros((n, n), dtype=int)  # rows=points, cols=lines
    for li, L in enumerate(lines):
        for pt_idx in L:
            M[pt_idx, li] = 1

    print("Lines (as point-index subsets):")
    for li, L in enumerate(lines):
        print(f"  line {li}: {sorted(L)}")

    print("\nIncidence matrix (7x7):")
    print(M)

    # axiom checks via matrix algebra: M M^T should have q+1=3 on diagonal
    # and exactly 1 off-diagonal (two points share exactly one line)
    MMt = M @ M.T
    diag_ok = all(MMt[i, i] == 3 for i in range(n))
    offdiag_ok = all(MMt[i, j] == 1 for i in range(n) for j in range(n) if i != j)
    print(f"\nM @ M^T diagonal == 3 (lines per point): {'PASS' if diag_ok else 'FAIL'}")
    print(f"M @ M^T off-diagonal == 1 (shared line per point-pair): {'PASS' if offdiag_ok else 'FAIL'}")
    assert diag_ok and offdiag_ok

    MtM = M.T @ M
    diag_ok2 = all(MtM[i, i] == 3 for i in range(n))
    offdiag_ok2 = all(MtM[i, j] == 1 for i in range(n) for j in range(n) if i != j)
    print(f"M^T @ M diagonal == 3 (points per line): {'PASS' if diag_ok2 else 'FAIL'}")
    print(f"M^T @ M off-diagonal == 1 (shared point per line-pair): {'PASS' if offdiag_ok2 else 'FAIL'}")
    assert diag_ok2 and offdiag_ok2

    eigvals = np.linalg.eigvalsh(MMt.astype(float))
    eigvals_sorted = sorted(eigvals, reverse=True)
    print(f"\nEigenvalues of M M^T: {[round(e, 4) for e in eigvals_sorted]}")

    # For a symmetric (v,k,lambda)-design incidence structure here k=lambda+... actually
    # for projective plane: MM^T = (q+1-1)I + J = q*I + J (since diag=q+1, offdiag=1)
    # eigenvalues of q*I + J on R^n: top eigenvalue = q + n (eigenvector=all-ones),
    # remaining eigenvalues = q (multiplicity n-1), since J has eigenvalues n (once) and 0 (n-1 times).
    q = 2
    expected_top = q + n  # 2 + 7 = 9
    expected_rest = q     # 2, multiplicity n-1=6
    top_eig = max(eigvals_sorted)
    rest_eigs = [e for e in eigvals_sorted if not np.isclose(e, expected_top, atol=1e-6)]

    top_ok = np.isclose(top_eig, expected_top, atol=1e-6)
    rest_ok = all(np.isclose(e, expected_rest, atol=1e-6) for e in rest_eigs) and len(rest_eigs) == n - 1

    print(f"Top eigenvalue = q+n = {expected_top}: {'PASS' if top_ok else 'FAIL'} (got {top_eig:.4f})")
    print(f"Remaining {n-1} eigenvalues = q = {expected_rest}: {'PASS' if rest_ok else 'FAIL'} "
          f"(got {[round(e,4) for e in rest_eigs]})")
    print(f"Relation to q+1=3: eigenvalue structure is q*I + J with q=2=(q+1)-1, "
          f"diagonal of MM^T itself = q+1 = 3 (lines/point) directly verified above.")

    assert top_ok, "Top eigenvalue mismatch"
    assert rest_ok, "Remaining eigenvalues mismatch"

    print("\nPart 5 overall: PASS")
    return True


