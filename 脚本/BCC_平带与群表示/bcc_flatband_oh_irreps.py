def vertex_coordinates(v):
    """
    Return the 'geometric' coordinates of a vertex for Oh action.
    The Oh group is centered at (1.5, 1.5, 1.5) (center of the 3x3x3 cube).
    BC vertices: ('bc', i, j, k) -> (i+0.5, j+0.5, k+0.5) -> centered: (i-1, j-1, k-1)
    Corner vertices: ('c', cx, cy, cz) -> (cx, cy, cz) -> centered: (cx-1.5, cy-1.5, cz-1.5)
    """
    if v[0] == 'bc':
        _, i, j, k = v
        return np.array([i - 1.0, j - 1.0, k - 1.0], dtype=float)
    else:
        _, cx, cy, cz = v
        return np.array([cx - 1.5, cy - 1.5, cz - 1.5], dtype=float)


def apply_oh_to_vertex(g, v):
    """
    Apply Oh element g (3x3 integer matrix, acts on centered coordinates)
    to vertex v, then unwrap back to the lattice representation.

    Returns the transformed vertex (as ('bc',...) or ('c',...) tuple),
    possibly in a different BC cell. We also return which BC cell
    the image would belong to (for the face's BC assignment).
    """
    coord = vertex_coordinates(v)
    new_coord = g @ coord

    if v[0] == 'bc':
        # BC vertex: centered coord = (i-1, j-1, k-1) for i,j,k in {0,1,2}.
        # new_coord should also be in {-1, 0, 1}^3 on the torus.
        # Unwrap: new_coord + 1 = (i',j',k') in {0,1,2} mod 3.
        i_new = int(round(new_coord[0] + 1.0)) % 3
        j_new = int(round(new_coord[1] + 1.0)) % 3
        k_new = int(round(new_coord[2] + 1.0)) % 3
        return ('bc', i_new, j_new, k_new)
    else:
        # Corner vertex: centered coord = (cx-1.5, cy-1.5, cz-1.5).
        # new_coord should be in {-1.5, -0.5, 0.5, 1.5}^3.
        # Unwrap to (cx', cy', cz') in {0,1,2}: cx' = new_coord[0] + 1.5 (mod 3).
        cx_new = int(round(new_coord[0] + 1.5)) % 3
        cy_new = int(round(new_coord[1] + 1.5)) % 3
        cz_new = int(round(new_coord[2] + 1.5)) % 3
        return ('c', cx_new, cy_new, cz_new)


def apply_oh_to_face(g, face):
    """Apply Oh element g to all vertices of a face. Returns transformed face as frozenset."""
    return frozenset(apply_oh_to_vertex(g, v) for v in face)


def which_bc_owns_face(face):
    """
    Determine which BC cell owns this face (by the BC vertex in the face, if any).
    If no BC vertex, infer from corner coordinates.

    For a face from the reference BC(0,0,0)'s 20 faces after Oh rotation:
    the BC vertex in the transformed face tells us which BC it ended up in.
    """
    for v in face:
        if v[0] == 'bc':
            return (v[1], v[2], v[3])
    # No BC vertex: this face is a pure corner face (3 corner vertices).
    # Determine ownership from which simplex it belongs to.
    # This is trickier; we'll handle it after building the face lookup.
    return None


def build_oh_permutation(g, ref_faces, ref_face_to_local_idx, face_to_idx, bc_face_indices, body_centers):
    """
    Build the 20x20 permutation matrix for Oh element g acting on the reference
    BC(0,0,0)'s 20 faces.

    For each reference face f_m (m=0..19):
      1. Apply g to get face g(f_m).
      2. g(f_m) belongs to some BC(i,j,k) in the lattice.
      3. Translate g(f_m) back to BC(0,0,0) by shift -(i,j,k) mod 3.
      4. The translated face must be one of the 20 reference faces -> local index n.
      5. P[n, m] = 1.

    This gives the effective 20x20 action on the internal face space.

    Returns: P (20x20 numpy array, permutation matrix).
    """
    P = np.zeros((20, 20), dtype=float)

    for m, face in enumerate(ref_faces):
        # Apply g
        g_face = apply_oh_to_face(g, face)

        # Find which BC owns this transformed face
        bc_owner = which_bc_owns_face(g_face)

        if bc_owner is None:
            # Pure corner face -- need to find it in face_to_idx and determine BC from there
            # This shouldn't happen for the reference BC faces (each has 1 BC vertex),
            # but handle gracefully.
            if g_face in face_to_idx:
                global_idx = face_to_idx[g_face]
                # Find which BC has this global index
                for bc_idx, bc_ijk in enumerate(body_centers):
                    if global_idx in bc_face_indices[bc_idx]:
                        bc_owner = bc_ijk
                        break
            if bc_owner is None:
                raise ValueError(f"Cannot find BC owner for face {g_face}")

        # Translate back to BC(0,0,0) by applying shift -(bc_owner)
        inv_shift = tuple((-s) % 3 for s in bc_owner)
        translated_face = translate_face(g_face, inv_shift)

        # Look up in reference face set
        if translated_face not in ref_face_to_local_idx:
            raise ValueError(
                f"Translated face {translated_face} not in reference face set.\n"
                f"  g={g}, face={face}, g_face={g_face}, bc_owner={bc_owner}"
            )

        n = ref_face_to_local_idx[translated_face]
        P[n, m] = 1.0

    # Verify it's a permutation matrix
    row_sums = P.sum(axis=1)
    col_sums = P.sum(axis=0)
    assert np.allclose(row_sums, 1.0), f"P not a permutation matrix (row sums: {row_sums})"
    assert np.allclose(col_sums, 1.0), f"P not a permutation matrix (col sums: {col_sums})"

    return P


