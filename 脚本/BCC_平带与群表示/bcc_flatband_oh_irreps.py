def generate_oh_elements():
    """
    Generate all 48 elements of Oh as 3x3 integer matrices.

    Oh = O x {I, -I} where O is the rotation group of the cube (24 elements).
    O is generated by: C4z (90-deg rotation about z) and C3_111 (120-deg about [1,1,1]).

    C4z: (x,y,z) -> (-y, x, z)
    C3_111: (x,y,z) -> (z, x, y)

    We generate O by repeated composition, then add inversion to get Oh.
    """
    # Define generators as 3x3 integer matrices
    C4z = np.array([[0, -1, 0],
                    [1,  0, 0],
                    [0,  0, 1]], dtype=int)

    C3_111 = np.array([[0, 0, 1],
                       [1, 0, 0],
                       [0, 1, 0]], dtype=int)

    # Generate O by BFS/closure
    def mat_to_tuple(m):
        return tuple(m.flatten())

    O_set = {}
    queue = [np.eye(3, dtype=int)]
    O_set[mat_to_tuple(np.eye(3, dtype=int))] = np.eye(3, dtype=int)

    generators = [C4z, C3_111, C4z.T, C3_111.T]  # include inverses for faster closure

    while queue:
        current = queue.pop(0)
        for g in generators:
            new = g @ current
            key = mat_to_tuple(new)
            if key not in O_set:
                O_set[key] = new
                queue.append(new)

    assert len(O_set) == 24, f"Expected 24 rotations, got {len(O_set)}"

    # Add inversion: Oh = O union (-I)*O
    inv = -np.eye(3, dtype=int)
    Oh_elements = []
    for key, mat in O_set.items():
        Oh_elements.append(mat.copy())
        Oh_elements.append((inv @ mat).copy())

    assert len(Oh_elements) == 48, f"Expected 48 Oh elements, got {len(Oh_elements)}"
    return Oh_elements


