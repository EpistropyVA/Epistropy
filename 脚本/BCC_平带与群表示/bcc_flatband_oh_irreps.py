def classify_oh_element(g):
    """
    Classify a 3x3 integer Oh matrix into its conjugacy class.

    Oh conjugacy classes (10 total):
      E    (1):  proper,  trace=3   (identity)
      C3   (8):  proper,  trace=0   (120-deg about body diag)
      C2   (6):  proper,  trace=-1  (180-deg about face diag)
      C4   (6):  proper,  trace=1   (90-deg about face normal)
      C2'  (3):  proper,  trace=-1  (180-deg about face normal -- but wait,
                                      same trace as C2 class)
      i    (1):  improper, trace=-3  (inversion)
      S6   (8):  improper, trace=0   (= i*C3)
      sigma_h(6): improper, trace=1  (reflection through face-normal plane)
      S4   (6):  improper, trace=-1  (= i*C4, but trace -1... let me re-examine)
      sigma_d(3): improper, trace=3  ... no

    Actually the correct classification for Oh uses det and trace:
      Proper (det=+1):
        E:   trace = 3
        C3:  trace = 0   (8 elements, body diagonals ±120, ±240)
        C2:  trace = -1  (6 elements, face-diagonal C2 axes)
        C4:  trace = 1   (6 elements, face-normal C4 axes, ±90 deg)
        C2': trace = -1  (3 elements, face-normal C2 axes, 180 deg)
      Wait -- C2 and C2' both have trace -1; we need another distinguisher.

    For det=+1, trace=-1: 9 elements total.
      C2  (6): rotation by 180 about a face-diagonal axis [1,1,0] type
      C2' (3): rotation by 180 about a face-normal axis [1,0,0] type

    Distinguishing: the axis type.
    C2' axes are {±x, ±y, ±z} (3 axes, 3 rotations by 180).
    C2  axes are {[1,±1,0] type} (6 axes, 6 rotations by 180).

    For det=+1, trace=-1: check if axis is ±e_i (face-normal, C2') or diagonal (C2).
    The rotation axis is the eigenvector with eigenvalue +1.
    For C2': axis is a coordinate axis -> exactly one component of the
             unnormalized eigenvec is nonzero in coord directions.
    Alternative: for a 180-deg rotation R about unit axis n,
      R = 2*n*n^T - I  => trace = 2|n|^2 - 3 = 2-3 = -1 (for unit n). Consistent.
      The eigenvector with eigenvalue +1 is n.

    Simpler: for det=+1, trace=-1 elements:
      C2' (3 elements): the matrix has exactly one +1 on diagonal and two -1s
                        (corresponding to reflection through two coord planes, i.e.,
                         rotation by 180 about a coord axis).
                        BUT: 180-deg rotation about x: diag(1,-1,-1), trace=-1 YES.
                        And: 180-deg rotation about [1,1,0]/sqrt(2):
                          R = [[0,1,0],[1,0,0],[0,0,-1]], trace=-1+0+0... wait trace=0+0-1=-1. Hmm.
                          Actually: R_[1,1,0] = [[0,1,0],[1,0,0],[0,0,-1]]:
                          trace = 0+0+(-1) = -1. Same trace.

    Let me just enumerate: there are 9 proper rotations with trace=-1 in O.
    Among these, 3 are C2' (axes ±x,±y,±z) and 6 are C2 (face-diagonal axes).
    C2' matrix for axis x = diag(1,-1,-1) -- has diagonal entries only.
    C2 matrix for axis [1,1,0]: R[i,j] = 2*n_i*n_j - delta_ij
      n = [1,1,0]/sqrt(2): R = [[0,1,0],[1,0,0],[0,0,-1]]
    C2' always diagonal. So: if det=+1, trace=-1, matrix is diagonal -> C2'.

    Full classification:
      det=+1: trace=3 -> E (1)
              trace=0 -> C3 (8)
              trace=1 -> C4 (6)
              trace=-1, diagonal -> C2' (3)
              trace=-1, not diagonal -> C2 (6)
      det=-1: trace=-3 -> i (1)
              trace=0 -> S6 (8)   [= i * C3]
              trace=-1 -> sigma_h (6)  [= i * C4]
              trace=1, diagonal -> sigma_d (3)  [= i * C2']
              trace=1, not diagonal -> S4 (6)  [= i * C2]

    Wait, let me recheck sigma_d and S4:
      i * C2' (axis x) = diag(-1,-1,-1)*diag(1,-1,-1) = diag(-1,1,1).
        trace = -1+1+1 = 1. diagonal. -> sigma_d.
      i * C2 (axis [1,1,0]) = -R_[1,1,0] = [[0,-1,0],[-1,0,0],[0,0,1]].
        trace = 0+0+1 = 1. not diagonal. -> S4? Let me check:
        S4 = i * C4. C4 (axis z, 90-deg): [[0,-1,0],[1,0,0],[0,0,1]], trace=0+0+1=1.
        i*C4: [[0,1,0],[-1,0,0],[0,0,-1]], trace=0+0-1=-1. That's improper, trace=-1.
        So: det=-1, trace=-1 -> sigma_h (6 elements). Hmm that doesn't match.

    Let me just do it numerically and verify counts.
    """
    det = int(round(np.linalg.det(g)))  # +1 or -1
    tr = int(round(np.trace(g)))

    if det == 1:
        if tr == 3:
            return 'E'
        elif tr == 0:
            return 'C3'
        elif tr == 1:
            return 'C4'
        elif tr == -1:
            # Distinguish C2 vs C2': diagonal matrix?
            off_diag = np.abs(g - np.diag(np.diag(g))).sum()
            if off_diag < 0.5:
                return "C2'"
            else:
                return 'C2'
        elif tr == -3:
            # Only happens if all eigenvalues are -1 and det=+1... impossible for 3x3 real.
            # This shouldn't occur in O.
            return 'UNKNOWN_proper'
        else:
            return f'UNKNOWN_proper_tr{tr}'
    else:  # det == -1
        # Improper elements. Classification:
        #
        # Trace-based (det=-1):
        #   i:       trace = -3
        #   S6:      trace =  0  (= i*C3, order 6)
        #   S4:      trace = -1  (= i*C4, order 4, g^2 is proper C2)
        #   sigma_h: trace =  1, non-diagonal (= i*C2 face-diagonal, 6 elements)
        #   sigma_d: trace =  1, diagonal     (= i*C2' face-normal,  3 elements)
        #
        # Derivation:
        #   C4(z) = [[0,-1,0],[1,0,0],[0,0,1]], trace=1.
        #   i*C4(z) = [[0,1,0],[-1,0,0],[0,0,-1]], trace=-1, det=-1. -> S4.
        #   C2'(x) = diag(1,-1,-1), trace=-1, diagonal.
        #   i*C2'(x) = diag(-1,1,1), trace=1, diagonal. -> sigma_d.
        #   C2([1,1,0]) = [[0,1,0],[1,0,0],[0,0,-1]], trace=-1, non-diagonal.
        #   i*C2([1,1,0]) = [[0,-1,0],[-1,0,0],[0,0,1]], trace=1, non-diagonal. -> sigma_h.
        #
        # Class sizes: S4(6), sigma_h(6), sigma_d(3) -- consistent with char table.
        if tr == -3:
            return 'i'
        elif tr == 0:
            return 'S6'
        elif tr == -1:
            return 'S4'
        elif tr == 1:
            # sigma_h (6 elements, non-diagonal = i*C2) or sigma_d (3 elements, diagonal = i*C2')
            off_diag = np.abs(g - np.diag(np.diag(g))).sum()
            if off_diag < 0.5:
                return 'sigma_d'
            else:
                return 'sigma_h'
        else:
            return f'UNKNOWN_improper_tr{tr}'


def verify_oh_classification(oh_elements):
    """Verify that the 48 elements split into the correct conjugacy class sizes."""
    classes = defaultdict(list)
    for g in oh_elements:
        cls = classify_oh_element(g)
        classes[cls].append(g)

    expected = {
        'E': 1, 'C3': 8, 'C2': 6, 'C4': 6, "C2'": 3,
        'i': 1, 'S6': 8, 'sigma_h': 6, 'S4': 6, 'sigma_d': 3
    }

    log("  Oh element classification:")
    ok = True
    for cls, exp in sorted(expected.items()):
        got = len(classes.get(cls, []))
        status = "OK" if got == exp else f"WRONG (expected {exp})"
        log(f"    {cls:10s}: {got:2d}  {status}")
        if got != exp:
            ok = False

    unknowns = [cls for cls in classes if cls not in expected]
    if unknowns:
        log(f"  UNKNOWN classes: {unknowns}")
        ok = False

    log(f"  Total: {sum(len(v) for v in classes.values())} (expect 48)")
    log(f"  Classification correct? {ok}")
    return classes, ok


